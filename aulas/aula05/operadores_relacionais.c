#include <stdio.h>

int main () {
    // < menor que 
    // <= menor igual
    // > maior que 
    // >= maior igual
    // == igual
    // != diferente

    printf("2 eh maior que 5? %i\n", 2 > 5);
    printf("2 eh maior igual 5? %i\n", 2 >= 5);
    printf("2 eh menor que 5? %i\n", 2 < 5);
    printf("2 eh menor igual 5? %i\n", 2 <= 5);
    printf("2 eh iguual 5? %i\n", 2 == 5);
    printf("2 eh diferente 5? %i\n", 2 != 5);


    return 0;
}